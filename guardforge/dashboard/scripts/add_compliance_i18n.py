MESSAGES_DIR = Path(__file__).resolve().parent.parent / "src" / "messages"


def main() -> None:
    if not MESSAGES_DIR.is_dir():
        raise SystemExit(f"messages dir not found: {MESSAGES_DIR}")

    updated: list[str] = []
    for lang in NAV_LABEL.keys():
        path = MESSAGES_DIR / f"{lang}.json"
        if not path.exists():
            print(f"SKIP missing: {path}")
            continue
        data = json.loads(path.read_text(encoding="utf-8"))
        # Add nav.compliance
        nav = data.setdefault("nav", {})
        nav["compliance"] = NAV_LABEL[lang]
        # Add compliance section
        data["compliance"] = COMPLIANCE_PAGE[lang]
        path.write_text(
            json.dumps(data, ensure_ascii=False, indent=2) + "\n",
            encoding="utf-8",
        )
        updated.append(lang)

    print(f"updated {len(updated)} files: {', '.join(updated)}")


if __name__ == "__main__":
    main()
